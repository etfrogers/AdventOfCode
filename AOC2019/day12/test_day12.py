STEP_PATTERN = re.compile(r'After (\d+) steps?:')


def parse_n_steps(string):
    matches = STEP_PATTERN.match(string)
    assert len(matches.groups()) == 1
    return int(matches.group(1))


def parse_expected_state(string):
    for entry in string.split('\n\n'):
        header, *moon_strings = entry.split('\n')
        n = parse_n_steps(header)
        yield n, moon_strings


def build_expected_states():
    for input_, output in zip((test_input_1, test_input_2), (test_output_1, test_output_2)):
        for n, state in parse_expected_state(output):
            yield input_, n, state


@pytest.mark.parametrize('input_, n, state', build_expected_states())
def test_1(input_, n, state):
    moons = MoonSet(input_)
    moons.run(n)
    expected = MoonSet.build_from_test_data(state)
    assert moons == expected


